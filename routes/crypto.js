// POST /crypto/pgp/envelope — PGP-style hybrid encryption
// Encrypts data with AES, then encrypts the AES key with recipient's RSA public key
router.post("/crypto/pgp/envelope", (req, res) => {
  try {
    const { plaintext, recipientPublicKey } = req.body;
    if (!plaintext || !recipientPublicKey) {
      return res.status(400).json({ error: "plaintext and recipientPublicKey required" });
    }

    // Generate random AES key
    const aesKey = crypto.randomBytes(32);
    const iv = crypto.randomBytes(12);

    // Encrypt data with AES
    const cipher = crypto.createCipheriv("aes-256-gcm", aesKey, iv);
    let ciphertext = cipher.update(plaintext, "utf8", "hex");
    ciphertext += cipher.final("hex");
    const authTag = cipher.getAuthTag().toString("hex");

    // Encrypt AES key with recipient's RSA public key
    const pubKeyPem = Buffer.from(recipientPublicKey, "base64").toString("utf8");
    const encryptedKey = crypto.publicEncrypt(
      { key: pubKeyPem, padding: crypto.constants.RSA_PKCS1_OAEP_PADDING, oaepHash: "sha256" },
      aesKey
    ).toString("base64");

    return res.json({
      ok: true,
      envelope: {
        encryptedKey,
        ciphertext,
        iv: iv.toString("hex"),
        authTag,
        algorithm: "PGP-style: RSA-OAEP-SHA256 + AES-256-GCM",
      },
      instructions: "Send the entire envelope to the recipient. They decrypt encryptedKey with their RSA private key, then use it to decrypt ciphertext.",
      ...royaltyStamp(),
    });
  } catch (err) {
    console.error("pgp envelope error:", err);
    return res.status(500).json({ error: "PGP envelope encryption failed" });
  }
});

// POST /crypto/pgp/open — Open a PGP-style envelope
router.post("/crypto/pgp/open", (req, res) => {
  try {
    const { envelope, privateKey } = req.body;
    if (!envelope || !privateKey) {
      return res.status(400).json({ error: "envelope and privateKey required" });
    }

    const { encryptedKey, ciphertext, iv, authTag } = envelope;

    // Decrypt AES key with RSA private key
    const privKeyPem = Buffer.from(privateKey, "base64").toString("utf8");
    const aesKey = crypto.privateDecrypt(
      { key: privKeyPem, padding: crypto.constants.RSA_PKCS1_OAEP_PADDING, oaepHash: "sha256" },
      Buffer.from(encryptedKey, "base64")
    );

    // Decrypt data with AES key
    const decipher = crypto.createDecipheriv("aes-256-gcm", aesKey, Buffer.from(iv, "hex"));
    decipher.setAuthTag(Buffer.from(authTag, "hex"));
    let plaintext = decipher.update(ciphertext, "hex", "utf8");
    plaintext += decipher.final("utf8");

    return res.json({ ok: true, plaintext });
  } catch (err) {
    return res.status(400).json({ error: "Failed to open envelope — wrong key or tampered data" });
  }
});

