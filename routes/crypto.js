// POST /crypto/keypair — Generate any type of keypair
router.post("/crypto/keypair", (req, res) => {
  try {
    const { algorithm, agentName, walletAddress } = req.body;
    const algo = (algorithm || "ed25519").toLowerCase();

    let publicKey, privateKey, pubHex, privHex, keyType;

    if (algo === "ed25519") {
      const pair = crypto.generateKeyPairSync("ed25519");
      pubHex = pair.publicKey.export({ type: "spki", format: "der" }).toString("hex");
      privHex = pair.privateKey.export({ type: "pkcs8", format: "der" }).toString("hex");
      keyType = "Ed25519";
    } else if (algo === "rsa" || algo === "rsa-4096") {
      const pair = crypto.generateKeyPairSync("rsa", {
        modulusLength: 4096,
        publicKeyEncoding: { type: "spki", format: "pem" },
        privateKeyEncoding: { type: "pkcs8", format: "pem" },
      });
      pubHex = Buffer.from(pair.publicKey).toString("base64");
      privHex = Buffer.from(pair.privateKey).toString("base64");
      keyType = "RSA-4096";
    } else if (algo === "rsa-2048") {
      const pair = crypto.generateKeyPairSync("rsa", {
        modulusLength: 2048,
        publicKeyEncoding: { type: "spki", format: "pem" },
        privateKeyEncoding: { type: "pkcs8", format: "pem" },
      });
      pubHex = Buffer.from(pair.publicKey).toString("base64");
      privHex = Buffer.from(pair.privateKey).toString("base64");
      keyType = "RSA-2048";
    } else if (algo === "x25519") {
      const pair = crypto.generateKeyPairSync("x25519");
      pubHex = pair.publicKey.export({ type: "spki", format: "der" }).toString("hex");
      privHex = pair.privateKey.export({ type: "pkcs8", format: "der" }).toString("hex");
      keyType = "X25519 (Diffie-Hellman)";
    } else if (algo === "ec" || algo === "ecdsa") {
      const pair = crypto.generateKeyPairSync("ec", { namedCurve: "prime256v1" });
      pubHex = pair.publicKey.export({ type: "spki", format: "der" }).toString("hex");
      privHex = pair.privateKey.export({ type: "pkcs8", format: "der" }).toString("hex");
      keyType = "ECDSA (P-256)";
    } else {
      return res.status(400).json({
        error: "Unsupported algorithm",
        supported: ["ed25519", "rsa-2048", "rsa-4096", "x25519", "ec"],
      });
    }

    const fingerprint = crypto.createHash("sha256").update(pubHex).digest("hex").slice(0, 16);
    const createdAt = new Date().toISOString();

    return res.status(201).json({
      ok: true,
      keypair: {
        algorithm: keyType,
        publicKey: pubHex,
        privateKey: privHex,
        fingerprint,
        createdAt,
        agentName: agentName || null,
        walletAddress: walletAddress || null,
      },
      soulMdBlock: [
        `## Cryptographic Identity (${keyType})`,
        "",
        `- **Agent:** ${agentName || "unnamed"}`,
        `- **Public Key:** ${pubHex.slice(0, 80)}${pubHex.length > 80 ? "..." : ""}`,
        `- **Fingerprint:** ${fingerprint}`,
        `- **Algorithm:** ${keyType}`,
        `- **Created:** ${createdAt}`,
        walletAddress ? `- **Wallet:** ${walletAddress}` : null,
        "",
        "Generated by Phantom Capital Cryptography Services",
        "https://phantomskills.zeabur.app",
      ].filter(Boolean).join("\n"),
      ...royaltyStamp(),
    });
  } catch (err) {
    console.error("keypair error:", err);
    return res.status(500).json({ error: "Key generation failed" });
  }
});

// POST /crypto/encrypt — AES-256-GCM encryption
router.post("/crypto/encrypt", (req, res) => {
  try {
    const { plaintext, key } = req.body;
    if (!plaintext) return res.status(400).json({ error: "plaintext required" });

    // Generate or use provided key
    const aesKey = key
      ? Buffer.from(key, "hex")
      : crypto.randomBytes(32);
    const iv = crypto.randomBytes(12);

    const cipher = crypto.createCipheriv("aes-256-gcm", aesKey, iv);
    let encrypted = cipher.update(plaintext, "utf8", "hex");
    encrypted += cipher.final("hex");
    const authTag = cipher.getAuthTag().toString("hex");

    return res.json({
      ok: true,
      ciphertext: encrypted,
      iv: iv.toString("hex"),
      authTag,
      algorithm: "AES-256-GCM",
      key: key ? undefined : aesKey.toString("hex"),
      note: key ? undefined : "New key generated — store it securely. Shown ONCE.",
      ...royaltyStamp(),
    });
  } catch (err) {
    console.error("encrypt error:", err);
    return res.status(500).json({ error: "Encryption failed" });
  }
});

// POST /crypto/decrypt — AES-256-GCM decryption
router.post("/crypto/decrypt", (req, res) => {
  try {
    const { ciphertext, key, iv, authTag } = req.body;
    if (!ciphertext || !key || !iv || !authTag) {
      return res.status(400).json({ error: "ciphertext, key, iv, and authTag required" });
    }

    const decipher = crypto.createDecipheriv(
      "aes-256-gcm",
      Buffer.from(key, "hex"),
      Buffer.from(iv, "hex")
    );
    decipher.setAuthTag(Buffer.from(authTag, "hex"));
    let decrypted = decipher.update(ciphertext, "hex", "utf8");
    decrypted += decipher.final("utf8");

    return res.json({ ok: true, plaintext: decrypted });
  } catch (err) {
    return res.status(400).json({ error: "Decryption failed — wrong key or tampered data" });
  }
});

// POST /crypto/hash — Hash data with any algorithm
router.post("/crypto/hash", (req, res) => {
  try {
    const { data, algorithm } = req.body;
    if (!data) return res.status(400).json({ error: "data required" });

    const algo = (algorithm || "sha256").toLowerCase();
    const supported = ["sha256", "sha512", "sha3-256", "sha3-512", "blake2b512", "md5"];

    if (!supported.includes(algo)) {
      return res.status(400).json({ error: "Unsupported algorithm", supported });
    }

    const hash = crypto.createHash(algo).update(data).digest("hex");

    return res.json({
      ok: true,
      hash,
      algorithm: algo,
      inputLength: data.length,
    });
  } catch (err) {
    console.error("hash error:", err);
    return res.status(500).json({ error: "Hashing failed" });
  }
});

